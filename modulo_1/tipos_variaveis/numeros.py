# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro) 

#Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real) 

# Type()
print("Tipo da varial real = ", type(numero_real))
print("Tipo da varial inteiro = ", type(numero_inteiro))

# soma +
soma = 1 + 1
print ("1 + 1 = ", soma)

# subtracao -
subtracao = 1 - 1
print ("1 - 1 = ", soma)

# multiplicacao *
multiplicacao = 1 * 1
print ("1 x 1 = ", multiplicacao)

# divisao *
divisao = 5 / 2
print ("5 / 2 = ", divisao)
print("tipo da variavel do resultado da divisao = ", type(divisao))

#float()
print("Valor em inteiro  = ", int(divisao))

#int()
print("Valor em float  = ", int(soma))

#Modulo %
modulo = 5 % 2
print("Modulo = ", modulo)

# divisao inteiro //
divisao = 5 / 2
print ("5 / 2 = ", divisao)
print("tipo da variavel do resultado da divisao = ", type(divisao))